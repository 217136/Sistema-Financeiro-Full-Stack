CREATE TABLE TB_COTACAO (
    DATA_COTACAO DATE NOT NULL,
    ID_MOEDA NUMBER NOT NULL,
    PRECO_COMPRA NUMBER(10, 4) NOT NULL,
    PRECO_VENDA NUMBER(10, 4) NOT NULL,
    VOLUME NUMBER(15, 2),
    
    CONSTRAINT PK_COTACAO PRIMARY KEY (DATA_COTACAO, ID_MOEDA),
    CONSTRAINT FK_COTACAO_MOEDA FOREIGN KEY (ID_MOEDA) REFERENCES TB_MOEDA(ID_MOEDA)
);

