-- Criação da Tabela Domínio
CREATE TABLE TB_MOEDA (
    ID_MOEDA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME_MOEDA VARCHAR2(50) NOT NULL UNIQUE,
    SIMBOLO VARCHAR2(5) NOT NULL
);

